    # ── Dataset generation ────────────────────────────────────────────────────

    def generate_dataset(self, size: int = 6000) -> pd.DataFrame:
        """
        Generate a balanced training dataset.

        Each class receives:
          - 200 samples from REAL_WORLD_PATTERNS  (diverse, realistic)
          - remainder from template-based generation

        Total dataset = 5 × (200 real + N template) logs, shuffled.
        """
        severity_levels  = list(self.log_templates.keys())
        real_per_class   = 200
        template_per_class = max(0, (size // len(severity_levels)) - real_per_class)

        logs = []
        for severity in severity_levels:
            # Real-world samples
            for _ in range(real_per_class):
                logs.append(self.generate_real_world_sample(severity))
            # Template samples
            for _ in range(template_per_class):
                logs.append(self.generate_template_log(severity))

        # Top-up to exact size with random template logs
        while len(logs) < size:
            logs.append(self.generate_template_log())

        random.shuffle(logs)
        df = pd.DataFrame(logs)
        print(f"[OK] Generated {len(df)} training samples")
        print(f"     Real-world : {real_per_class * len(severity_levels)} ({real_per_class} x {len(severity_levels)} classes)")
        print(f"     Template   : {len(df) - real_per_class * len(severity_levels)}")
        print(f"[Stats] Distribution:\n{df['severity'].value_counts()}")
        return df


