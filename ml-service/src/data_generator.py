    # ── Real-world sample generation ──────────────────────────────────────────

    def generate_real_world_sample(self, severity: str) -> dict:
        """Pick a real-world pattern, apply light numeric variation, return a log dict."""
        patterns   = REAL_WORLD_PATTERNS[severity]
        pattern    = random.choice(patterns)
        augmentors = _NUMERIC_AUGMENTORS.get(severity, [])
        if augmentors and random.random() < 0.4:
            try:
                pattern = random.choice(augmentors)(pattern)
            except Exception:
                pass
        return {
            "message":   pattern,
            "severity":  severity,
            "source":    random.choice(self.modules),
            "timestamp": datetime.now().isoformat()
        }

